# Sort method makes  easy to saort a list in order
cars=['ferrari','benz','audi','ford','hyundai','chevrolet']
print(cars)
# Sorting method
cars.sort()
print(cars)

# using the reverse method
cars.sort(reverse=True)
print(cars)

# Priniting inreverse
cars.reverse()
print(cars)

# Length Of the List
print(len(cars))