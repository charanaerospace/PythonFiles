# Concatenation
# The process of combining two strings into a single one
# python uses the  + symbol to combine the strings
first_name = "ada"
last_name  = "lovelace"
full_name  = first_name + " " +last_name
print(full_name)

# using the camelcase method
first_name = "ada"
last_name  = "lovelace"
full_name  = first_name + " " +last_name
print("Hello, "+full_name.title()+"!")

