# String data type
# A group of characters enclosed on a single or double or triple quotations is called strings
# Example
# double quotes
a = "Hello, world!"
# single quotrs
b = 'Hello, world'
# Triple quotes
c = '''Hello, world'''
print(a)
print(b)
print(c)
# datatypes
print(type(a))
print(type(b))
print(type(c))