# Boolean data types
# bool data types represent boolean values in python
# boolean data type has two values
# a)True
# b)False
# Python internally represents 
# a)true as 1 
# b)false as 0
# Empty string represents false
# Example
a = True
b = False
# prints the  variable a,b
print(a)
print(b)
print(a+a)
print(a+b)
print(b+b)
# prints the type of the  variable
print(type(a))
print(type(b))
