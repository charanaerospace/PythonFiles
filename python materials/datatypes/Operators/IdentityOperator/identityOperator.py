# Identity Operator
# Compares the memory location to
# two elements or variables or operators
# we will be able to know tht the two objects
#  are poinitng the same location or not
# memory location can be seen using the id() function

# Example
a = 25
b = 25
# prints the variable
print(a)
print(b)
# print the location of the two objects
print(id(a))
print(id(b))